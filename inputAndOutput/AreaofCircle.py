# In geometry, the area enclosed by a circle of radius r is πr^2. Here the Greek letter π represents a constant, approximately equal to 3.142, 
# which is equal to the ratio of the circumference of any circle to its diameter.Subash wants to find the area of circle for the given radius. 
# Can you help him to calculate the area of circle for the given radius? 


r = float(input())
pi = 3.142
area = pi * (r **2)
print(f"The area of the circle with radius {r} is: {area}")